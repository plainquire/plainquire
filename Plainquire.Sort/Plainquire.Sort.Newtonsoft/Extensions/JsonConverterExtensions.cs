using Newtonsoft.Json;
using Plainquire.Sort.Newtonsoft.JsonConverters;
using System.Collections.Generic;

namespace Plainquire.Sort.Newtonsoft;

/// <summary>
/// Extension methods for <see cref="JsonConverter"/>.
/// </summary>
public static class JsonConverterExtensions
{
    /// <summary>
    /// List of Newtonsoft JSON converters required to serialize/deserialize sort queryable related classes.
    /// </summary>
    public static readonly JsonConverter[] NewtonsoftConverters =
    [
        new EntitySortConverter(),
    ];

    /// <summary>
    /// Adds support to serialize/deserialize sort queryable related classes.
    /// </summary>
    /// <param name="converters">The converters.</param>
    /// <autogeneratedoc />
    public static void AddSortNewtonsoftSupport(this IList<JsonConverter> converters)
    {
        foreach (var converter in NewtonsoftConverters)
            converters.Add(converter);
    }
}